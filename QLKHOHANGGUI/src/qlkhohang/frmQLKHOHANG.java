    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        Date ngayThue = new Date();
        Date ngayTra = new Date();
        SimpleDateFormat df = new SimpleDateFormat("dd/MM/yyyy");
        try {
            ngayThue = df.parse(txtNgayThue.getText());
        } catch (ParseException ex) {
            Logger.getLogger(frmQLKHOHANG.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            ngayTra = df.parse(txtNgayTra.getText());
        } catch (ParseException ex) {
            Logger.getLogger(frmQLKHOHANG.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (cbKhoNguyenLieu.isSelected()) {
            KHOHANG knl = new KHONGUYENLIEU(txtKhoNL_KhoSP.getText(), txtTenKhoHang.getText(), Double.parseDouble(txtKhaNangChua.getText()), Double.parseDouble(txtTonKho.getText()), ngayThue, ngayTra);
            dsKhoHang.themKhoHang(knl);
        } else {
            KHOHANG ksp = new KHOSANPHAM(txtKhoNL_KhoSP.getText(), txtTenKhoHang.getText(), Double.parseDouble(txtKhaNangChua.getText()), Double.parseDouble(txtTonKho.getText()), ngayThue, ngayTra);
            dsKhoHang.themKhoHang(ksp);
        }
        tbXuat.setModel(new MODELDATA(dsKhoHang.getList()));
        tbXuat.repaint();
    }//GEN-LAST:event_btnThemActionPerformed

