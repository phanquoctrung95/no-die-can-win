    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
        // TODO add your handling code here:
        KHOHANG kh;
        Date ngayThue = new Date();
        SimpleDateFormat df = new SimpleDateFormat("dd/MM/yyyy");
        try {
            ngayThue = df.parse(txtNgayThue.getText());
        } catch (ParseException ex) {
            Logger.getLogger(frmQLKHOHANG.class.getName()).log(Level.SEVERE, null, ex);
        }
        Date ngayTra = new Date();
        try {
            ngayTra = df.parse(txtNgayTra.getText());
        } catch (ParseException ex) {
            Logger.getLogger(frmQLKHOHANG.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (cbKhoNguyenLieu.isSelected()) {
            kh = new KHONGUYENLIEU(txtKhoNL_KhoSP.getText(), txtTenKhoHang.getText(), Double.parseDouble(txtKhaNangChua.getText()), Double.parseDouble(txtTonKho.getText()), ngayThue, ngayTra);
            dsKhoHang.themKhoHang(kh);
        } else {
            kh = new KHOSANPHAM(txtKhoNL_KhoSP.getText(), txtTenKhoHang.getText(), Double.parseDouble(txtKhaNangChua.getText()), Double.parseDouble(txtTonKho.getText()), ngayThue, ngayTra);
            dsKhoHang.themKhoHang(kh);
        }
        this.dsKhoHang.sua(kh);
        tbXuat.setModel(new MODELDATA(dsKhoHang.getList()));
        tbXuat.repaint();
    }//GEN-LAST:event_btnCapNhatActionPerformed

