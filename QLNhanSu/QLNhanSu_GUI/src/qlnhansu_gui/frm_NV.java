    private void btnTimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimActionPerformed
        // TODO add your handling code here:
        NHANVIEN nv = dsNhanViens.Tim(txtMaNV.getText());
        txtTenNV.setText(nv.getTenNV());
        txtMaNV.setText(nv.getMaNV());
        txtNgayVao.setText(nv.getNgayVao().toString());
        txtPhuCap.setText(String.valueOf(nv.PhuCap()));
        txtThucLinh.setText(String.valueOf(nv.ThucLinh()));
        if (nv instanceof BIENCHE) {
            BIENCHE bc = (BIENCHE) nv;
            txtNgayCong.setText(String.valueOf(bc.getHSL()));
        } else {
            HOPDONG hd = (HOPDONG) nv;
            txtNgayCong.setText(String.valueOf(hd.getSoNgayCong()));
        }
    }//GEN-LAST:event_btnTimActionPerformed

