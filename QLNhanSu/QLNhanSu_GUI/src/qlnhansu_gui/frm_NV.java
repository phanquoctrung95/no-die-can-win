    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        NHANVIEN nv;
        Date ngayVao = new Date();
        SimpleDateFormat df = new SimpleDateFormat("dd/MM/yyyy");
        try {
            ngayVao = df.parse(txtNgayVao.getText());
        } catch (ParseException ex) {
            Logger.getLogger(frm_NV.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (cbBienChe.isSelected()) {
            nv = new BIENCHE(Double.parseDouble(txtNgayCong.getText()), txtMaNV.getText(), txtTenNV.getText(), ngayVao);
        } else {
            nv = new HOPDONG(Integer.parseInt(txtNgayCong.getText()), txtMaNV.getText(), txtTenNV.getText(), ngayVao);
        }
        this.dsNhanViens.Sua(nv);
        tbNHV.setModel(new ModelData(dsNhanViens.getDs()));
        tbNHV.repaint();
    }//GEN-LAST:event_btnSuaActionPerformed

