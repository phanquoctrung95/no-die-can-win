    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        Date ngayVao = new Date();
        SimpleDateFormat df = new SimpleDateFormat("dd/MM/yyyy");
        try {
            ngayVao = df.parse(txtNgayVao.getText());
        } catch (ParseException ex) {
            Logger.getLogger(frm_NV.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (cbBienChe.isSelected()) {
            NHANVIEN nvbc = new BIENCHE(Integer.parseInt(txtNgayCong.getText()), txtMaNV.getText(), txtTenNV.getText(), ngayVao);
            dsNhanViens.Them(nvbc);
        } else {
            NHANVIEN nvhd = new HOPDONG(Integer.parseInt(txtNgayCong.getText()), txtMaNV.getText(), txtTenNV.getText(), ngayVao);
            dsNhanViens.Them(nvhd);
        }
        tbNHV.setModel(new ModelData(dsNhanViens.getDs()));
        tbNHV.repaint();
    }//GEN-LAST:event_btnThemActionPerformed

